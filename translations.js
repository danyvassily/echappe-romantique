// Fonction pour d√©tecter automatiquement la langue du navigateur
function detectBrowserLanguage() {
  // Obtenir les langues du navigateur
  const browserLanguages = navigator.languages || [
    navigator.language || navigator.browserLanguage || "fr",
  ];

  // Langues support√©es par notre site
  const supportedLanguages = ["fr", "en", "ru"];

  // Chercher la premi√®re langue support√©e
  for (let browserLang of browserLanguages) {
    // Extraire le code de langue (ex: 'en-US' -> 'en')
    const langCode = browserLang.split("-")[0].toLowerCase();

    // V√©rifier si la langue est support√©e
    if (supportedLanguages.includes(langCode)) {
      console.log(
        `üåç Langue d√©tect√©e automatiquement: ${langCode} (${browserLang})`
      );
      return langCode;
    }
  }

  // Si aucune langue support√©e d√©tect√©e, utiliser le fran√ßais par d√©faut
  console.log("üåç Langue par d√©faut: fran√ßais (fr)");
  return "fr";
}

// Fonction pour obtenir la langue actuellement s√©lectionn√©e
function getCurrentLanguage() {
  // Si l'utilisateur a d√©j√† fait un choix manuel, le respecter
  const savedLanguage = localStorage.getItem("selectedLanguage");
  if (savedLanguage) {
    return savedLanguage;
  }

  // Sinon, d√©tecter automatiquement et sauvegarder
  const detectedLanguage = detectBrowserLanguage();
  localStorage.setItem("selectedLanguage", detectedLanguage);
  return detectedLanguage;
}

// Fonction pour changer la langue
function changeLanguage(language) {
  // Sauvegarder le choix manuel de l'utilisateur
  localStorage.setItem("selectedLanguage", language);
  console.log(`üë§ Langue chang√©e manuellement vers: ${language}`);

  // Appliquer la nouvelle traduction
  translatePage();

  // Animation de transition fluide
  if (typeof gsap !== "undefined") {
    const elements = document.querySelectorAll("[data-translate]");
    gsap.fromTo(
      elements,
      { opacity: 0.7, y: 5 },
      {
        opacity: 1,
        y: 0,
        duration: 0.4,
        stagger: 0.02,
        ease: "power2.out",
      }
    );
  }
}

// Fonction pour traduire la page
function translatePage() {
  const currentLang = getCurrentLanguage();
  const elements = document.querySelectorAll("[data-translate]");

  elements.forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (translations[currentLang] && translations[currentLang][key]) {
      if (element.tagName === "INPUT" && element.type === "submit") {
        element.value = translations[currentLang][key];
      } else if (
        element.tagName === "INPUT" &&
        element.placeholder !== undefined
      ) {
        element.placeholder = translations[currentLang][key];
      } else {
        element.textContent = translations[currentLang][key];
      }
    }
  });

  // Mettre √† jour l'attribut lang de la page
  document.documentElement.setAttribute("lang", currentLang);

  // Mettre √† jour les liens selon la langue
  updateLanguageSpecificContent(currentLang);
}

// Fonction pour mettre √† jour le contenu sp√©cifique √† la langue
function updateLanguageSpecificContent(lang) {
  // Mettre √† jour les meta descriptions
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && translations[lang]["meta-description"]) {
    metaDesc.setAttribute("content", translations[lang]["meta-description"]);
  }

  // Mettre √† jour le titre de la page
  if (translations[lang]["page-title"]) {
    document.title = translations[lang]["page-title"];
  }
}

